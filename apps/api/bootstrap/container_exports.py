def load_api_container_exports() -> dict[str, Any]:
    api_app_context = import_module("apps.api.api_app_context")
    missing = [name for name in API_CONTAINER_EXPORTS if not hasattr(api_app_context, name)]
    if missing:
        formatted = ", ".join(missing)
        raise AttributeError(f"apps.api.api_app_context is missing required container exports: {formatted}")
    return {name: getattr(api_app_context, name) for name in API_CONTAINER_EXPORTS}
